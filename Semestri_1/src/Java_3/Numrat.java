package Java_3;

public class Numrat {
    public static void main(String[] args) {
        System.out.println("100");//     100 ne String
        System.out.println(100); //      100 ne int
        /*
            Output:
            100
            100
        */

        System.out.println("\n\n");

        System.out.println("15" + "17"); // bashkimi i stringut
        System.out.println(15 + 17);     //
        System.out.println("18" + 4);

        System.out.println("\n"); // new line

        System.out.println("35 + 32 = " + 35 + 32);     // 3523
        System.out.println("35 + 32 = " + (35 + 32));  // 67
    }
}
